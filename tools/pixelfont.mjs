export function px(png, x, y, r, g, b, a = 255) {
  if (x < 0 || y < 0 || x >= png.width || y >= png.height) return;
  const i = (y * png.width + x) * 4;
  png.data[i] = r; png.data[i + 1] = g; png.data[i + 2] = b; png.data[i + 3] = a;
}

/** Draw `str` with its top-left at (x,y); each font pixel is `s` screen px. */
export function text(png, str, x, y, s, rgb = [255, 255, 255]) {
  let cx = x;
  for (const ch of String(str).toUpperCase()) {
    const glyph = FONT[ch] ?? FONT["?"];
    for (let gy = 0; gy < 5; gy++) {
      for (let gx = 0; gx < 3; gx++) {
        if (glyph[gy][gx] !== "1") continue;
        for (let sy = 0; sy < s; sy++) {
          for (let sx = 0; sx < s; sx++) px(png, cx + gx * s + sx, y + gy * s + sy, rgb[0], rgb[1], rgb[2]);
        }
      }
    }
    cx += 4 * s;
  }
}

export const textW = (str, s) => String(str).length * 4 * s;
